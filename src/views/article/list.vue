<template>
  <div class="app-container">
    <el-table v-loading="listLoading" :data="list" border fit highlight-current-row style="width: 100%">
      <el-table-column
        align="center"
        type="index"
        width="50"
      >
      </el-table-column>
      <!-- <el-table-column align="center" label="ID" width="200">
